# CONJUNTO

frutas = {"maça", "banana", "uva"}
print(frutas)

# Adiciona elemento ao conjunto
frutas.add("morango")
print(frutas)

# Remove elemento do conjunto
frutas.remove("banana")
print(frutas)

# Tentar adicionar um elemento ja existente (VAI IGNORAR)
frutas.add("morango")
print(frutas)

